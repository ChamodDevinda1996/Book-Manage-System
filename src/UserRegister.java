private  void insert(String userName,String password,int idNumber,String email,int dob,String address,int mobileNo){
        
    
    try {

        String sql = "INSERT INTO users(userName,password,idNumber,email,dob,address,mobileNo) VALUES(?,?,?,?,?,?,?)";
        
        ps = con.prepareStatement(sql);
        ps.setString(1, userName);
        ps.setString(2, password);
        ps.setInt(3, idNumber);
        ps.setString(4, email);
        ps.setInt(5, dob);
        ps.setString(6, address);
        ps.setInt(7, mobileNo);
        ps.execute();
        
        JOptionPane.showMessageDialog(null, "Sucess!");
    } catch (Exception e) {
        System.out.println(e+"");
    }
}

