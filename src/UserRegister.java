    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String username     = this.username.getText();
        int idnumber        = 0;
        int dob             = 0;
        int mobileNum       = 0;
        String password     = this.password.getText();
        String email        = this. email.getText();
        String address      = this.address.getText();
        boolean valid       = false;
        
        try {
              idnumber = Integer.parseInt(this.username1.getText());
              dob = Integer.parseInt(this.dob.getText());
              mobileNum = Integer.parseInt(mobilenum.getText());
              valid =true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter a number for 'NIC Number','DOB' fields and 'Mobile Number' field");
            
        }
        
        
        if (username.compareTo("")==0  || !valid || password.compareTo("")==0 || email.compareTo("")==0  || address.compareTo("")==0 ) {
            
            JOptionPane.showMessageDialog(null, "Please Complete The Form" );
        }
        else{
            
        insert(username,password,idnumber,email, dob, address, mobileNum);

        this.username.setText("");
        this.password.setText("");
        this.email.setText("");
        this.dob.setText("");
        this.address.setText("");
        this.mobilenum.setText("+94");

        setVisible(false);
        this.dispose();
        UserLogin ob = new UserLogin();
        ob.setVisible(true);
        }
        

    }//GEN-LAST:event_jButton3ActionPerformed

